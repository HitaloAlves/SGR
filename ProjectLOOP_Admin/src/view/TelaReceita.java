    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        if (jTReceitas.getSelectedRow() != -1) {

            int check = JOptionPane.showConfirmDialog(null, "Confirma a exclusão", "Exclusão", JOptionPane.YES_NO_OPTION);

            if (check == 0) {

                Receita rec = new Receita();

                rec.setId_receita(Integer.parseInt(jTReceitas.getValueAt(jTReceitas.getSelectedRow(), 0).toString()));

                rec.deletarReceita();

                readTable();
            }

        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma Receita para exclusão");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        if (jTReceitas.getSelectedRow() != -1) {

            TelaAlterarReceita telaAltRec = new TelaAlterarReceita();

            telaAltRec.setId(Integer.parseInt(jTReceitas.getValueAt(jTReceitas.getSelectedRow(), 0).toString()));
            telaAltRec.setNome(jTReceitas.getValueAt(jTReceitas.getSelectedRow(), 1).toString());
            telaAltRec.setTipo_nome(jTReceitas.getValueAt(jTReceitas.getSelectedRow(), 2).toString());
            telaAltRec.setTema_nome(jTReceitas.getValueAt(jTReceitas.getSelectedRow(), 3).toString());
            telaAltRec.setMomento_nome(jTReceitas.getValueAt(jTReceitas.getSelectedRow(), 4).toString());
            telaAltRec.setOcasiao_nome(jTReceitas.getValueAt(jTReceitas.getSelectedRow(), 5).toString());
            telaAltRec.setIngredientes(jTReceitas.getValueAt(jTReceitas.getSelectedRow(), 6).toString());
            telaAltRec.setPreparacao(jTReceitas.getValueAt(jTReceitas.getSelectedRow(), 7).toString());
            telaAltRec.setTempoPreparo(jTReceitas.getValueAt(jTReceitas.getSelectedRow(), 8).toString());

            telaAltRec.setReceita();

            jDesktopPane1.add(telaAltRec);

            telaAltRec.setVisible(true);

        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma Receita para Alterar");
        }

    }//GEN-LAST:event_jButton3ActionPerformed

