    public void readTable() {
        DefaultTableModel modelo = (DefaultTableModel) jTEventos.getModel();

        modelo.setNumRows(0); // Limpando a tabela

        Evento rec = new Evento();

        for (EventoObjeto e : rec.listarEventos()) {

            modelo.addRow(new Object[]{
                e.getId_vento(),
                e.getNome(),
                e.getData_evento()
            });
        }
    }

    public void readTableForSearch(String search) {
        DefaultTableModel modelo = (DefaultTableModel) jTEventos.getModel();

        modelo.setNumRows(0); // Limpando a tabela

        Evento rec = new Evento();

        for (EventoObjeto e : rec.searchEventos(search)) { // Pesquisando no banco usando LIKE, Se existe algo no mesmo

            modelo.addRow(new Object[]{ // Adicionando dados na tabela
                e.getId_vento(),
                e.getNome(),
                e.getData_evento()
            });
        }
    }

    public boolean validCampos() {
        boolean check = true;

        if (nomeEvento.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha o campo Nome");
            check = false;
        } else if (jFormattedTextField1.getText().equals("  /  /    ")) {
            JOptionPane.showMessageDialog(null, "Preencha o campo Data do Evento");
            check = false;
        }

        return check;
    }

    public boolean criarEventoDadosAlterar() {
        boolean check;

        if (jTEventos.getSelectedRow() != -1) { // Se alguma tupla estiver selecionada na tabela

            int check2 = JOptionPane.showConfirmDialog(null, "Criar Evento com os mesmos dados de Alteração", "Alteração", JOptionPane.YES_NO_OPTION);

            check = check2 == 0; // Se check2 == 0, true - Criar evento com os dados de alteração

        } else {
            check = true; // True da própria criação do evento
        }

        return check;
    }

