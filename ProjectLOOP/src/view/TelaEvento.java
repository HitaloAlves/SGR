    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        if (validCampos()) {
            if (criarEventoDadosAlterar()) { // Condição em cascata
                
                Evento evento = new Evento();

                evento.setNome(nomeEvento.getText());
                evento.setData_evento(jFormattedTextField1.getText());

                if (evento.cadastrarEvento()) { // Se true, limpar os campos e recarregar a tabela eventos
                    nomeEvento.setText(null);
                    jFormattedTextField1.setText(null);

                    readTable();
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        if (jTEventos.getSelectedRow() != -1) {

            int check = JOptionPane.showConfirmDialog(null, "Confirma a exclusão", "Exclusão", JOptionPane.YES_NO_OPTION);

            if (check == 0) {
//                DefaultTableModel dtmEventos = (DefaultTableModel) jTEventos.getModel();
//                dtmEventos.removeRow(jTEventos.getSelectedRow());

                Evento evento = new Evento();

                evento.setId_vento(Integer.parseInt(jTEventos.getValueAt(jTEventos.getSelectedRow(), 0).toString()));

                evento.deletarEvento();

                readTable();
            }

        } else {
            JOptionPane.showMessageDialog(null, "Selecione um evento para exclusão");
        }


    }//GEN-LAST:event_jButton2ActionPerformed

