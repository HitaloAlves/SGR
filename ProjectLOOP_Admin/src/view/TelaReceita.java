    public void readTable() {
        DefaultTableModel modelo = (DefaultTableModel) jTReceitas.getModel();

        modelo.setNumRows(0);

        Receita rec = new Receita();

        for (ReceitasObjeto r : rec.listarReceitas()) {

            modelo.addRow(new Object[]{
                r.getId_receita(),
                r.getNome(),
                r.getTipo_receita(),
                r.getTema_receita(),
                r.getMomento_receita(),
                r.getOcasiao_receita(),
                r.getIngredientes(),
                r.getPreparação(),
                r.getTempoPreparo()

            });
        }
    }

