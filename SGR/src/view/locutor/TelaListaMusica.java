    public boolean veficarNomeIguaisLista() { // Se nomes forem iguais não alterar o nome

        boolean check = false;

        for (ObjetoListaMusica lm : this.objListaM) {

            if (nomeLista.getText().equals(lm.getNome())) {
                check = true;
            }

        }

        return check;
    }

