    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        if (validCampos()) { 
            // Objeto responsável por transformar a seleção de String para id da opção no banco de dados 

            TiposReceitas trec = new TiposReceitas();

            trec.setTipo_nome(jComboBox1.getSelectedItem().toString());
            trec.setTema_nome(jComboBox2.getSelectedItem().toString());
            trec.setMomento_nome(jComboBox3.getSelectedItem().toString());
            trec.setOcasiao_nome(jComboBox4.getSelectedItem().toString());

            List<Receita> tr = trec.returnTiposCadReceitas();

            // Cadastrar Receita
            Receita rec = new Receita();

            rec.setNome(textNome.getText());
            rec.setTipo_receita(tr.get(0).getTipo_receita());
            rec.setTema_receita(tr.get(0).getTema_receita());
            rec.setMomento_receita(tr.get(0).getMomento_receita());
            rec.setOcasiao_receita(tr.get(0).getOcasiao_receita());

            rec.setIngredientes(jTextArea1.getText());
            rec.setPreparação(jTextArea2.getText());

            rec.setTempoPreparo(jFormattedTextField1.getText());

            // Realizar Cadastro
            if (rec.cadastrarReceita()) {
                // Limpar campos 
                textNome.setText(null);
                jComboBox1.setSelectedItem("Selecione");
                jComboBox2.setSelectedItem("Selecione");
                jComboBox3.setSelectedItem("Selecione");
                jComboBox4.setSelectedItem("Selecione");
                jTextArea1.setText(null);
                jTextArea2.setText(null);
                jFormattedTextField1.setText(null);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

