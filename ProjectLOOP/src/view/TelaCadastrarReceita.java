    public boolean validCampos() {
        
        boolean check = true;

        if (textNome.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha o campo Nome");
            check = false;
        } else if (jComboBox1.getSelectedItem().toString().equals("Selecione")) {
            JOptionPane.showMessageDialog(null, "Selecione um Tipo para Receita");
            check = false;
        } else if (jComboBox2.getSelectedItem().toString().equals("Selecione")) {
            JOptionPane.showMessageDialog(null, "Selecione um Tema para Receita");
            check = false;
        } else if (jComboBox3.getSelectedItem().toString().equals("Selecione")) {
            JOptionPane.showMessageDialog(null, "Selecione um Momento para Receita");
            check = false;
        } else if (jComboBox4.getSelectedItem().toString().equals("Selecione")) {
            JOptionPane.showMessageDialog(null, "Selecione um Ocasião para Receita");
            check = false;
        } else if (jTextArea1.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha o campo Ingredientes");
            check = false;
        } else if (jTextArea2.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha o campo Modo de Preparo");
            check = false;
        } else if (jFormattedTextField1.getText().equals("  :  :  ")) {
            JOptionPane.showMessageDialog(null, "Preencha o campo Tempo de Preparo");
            check = false;
        }
        
        return check;
    }

